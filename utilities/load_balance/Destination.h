#ifndef __SPTK_DESTINATION_H__
#define __SPTK_DESTINATION_H__

#include <sptk5/cutils>
#ifndef _WIN32
#include <netinet/in.h>
#endif

class Destination
{
    sptk::String    m_host;
    uint16_t        m_port;
    sockaddr_in     m_address;
public:
    Destination(sptk::String host="", uint16_t port=0);
    Destination(const Destination& other);
    const sockaddr_in& address() const { return m_address; }
};

#endif
